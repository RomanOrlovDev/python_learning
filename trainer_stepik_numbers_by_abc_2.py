ss = """
x--------------------------------------------------x
| --        --   --        --   --   --   --   --  |
||  |    |    |    | |  | |    |       | |  | |  | |
||  |    |    |    | |  | |    |       | |  | |  | |
|           --   --   --   --   --        --   --  |
||  |    | |       |    |    | |  |    | |  |    | |
||  |    | |       |    |    | |  |    | |  |    | |
| --        --   --        --   --        --   --  |
x--------------------------------------------------x
""".split('\n')
ds = input()
for y in range(1,10):
    s = ss[y][0]
    for d in ds:
        x = 5 * int(d)
        s += ss[y][1 + x: 6 + x]
    print(s[0:-1] + ss[y][0])